package truecorp.core.model;

import java.math.BigDecimal;
import java.util.Date;

/**
* @author Supot Saelao
* @version 1.0
*/
public class BackoutRequestDto extends RestRequest {
	private static final long serialVersionUID = 1L;
	
	private Long ban;
	private Long entSeqNo;
	private Long actvSeqNo;
	private String actvCode;
	private String actvReasonCode;
	private Date actvDate;
	private BigDecimal actvAmt;
	private Long operatorId;

	public Long getBan() {
		return ban;
	}

	public void setBan(Long ban) {
		this.ban = ban;
	}

	public Long getEntSeqNo() {
		return entSeqNo;
	}

	public void setEntSeqNo(Long entSeqNo) {
		this.entSeqNo = entSeqNo;
	}

	public Long getActvSeqNo() {
		return actvSeqNo;
	}

	public void setActvSeqNo(Long actvSeqNo) {
		this.actvSeqNo = actvSeqNo;
	}

	public String getActvCode() {
		return actvCode;
	}

	public void setActvCode(String actvCode) {
		this.actvCode = actvCode;
	}

	public String getActvReasonCode() {
		return actvReasonCode;
	}

	public void setActvReasonCode(String actvReasonCode) {
		this.actvReasonCode = actvReasonCode;
	}

	public Date getActvDate() {
		return actvDate;
	}

	public void setActvDate(Date actvDate) {
		this.actvDate = actvDate;
	}

	public BigDecimal getActvAmt() {
		return actvAmt;
	}

	public void setActvAmt(BigDecimal actvAmt) {
		this.actvAmt = actvAmt;
	}

	public Long getOperatorId() {
		return operatorId;
	}

	public void setOperatorId(Long operatorId) {
		this.operatorId = operatorId;
	}

	@Override
	public String toString() {
		return "BackoutResponseDto [ban=" + ban + ", actvCode=" + actvCode + ", actvReasonCode=" + actvReasonCode
				+ ", actvDate=" + actvDate + ", actvAmt=" + actvAmt + ", operatorId=" + operatorId + "]";
	}

}
